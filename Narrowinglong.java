class Narrowinglong
{
	public static void main(String[] args) 
	{
		long a=6379888340l;
	    byte res2=(byte)a;
		short res3=(short)a;
		int res4=(int)a;
		char res6=(char)a;
		System.out.println(a);
		System.out.println(res2);
		System.out.println(res3);
		System.out.println(res4);
		System.out.println(res6);
	}
}
