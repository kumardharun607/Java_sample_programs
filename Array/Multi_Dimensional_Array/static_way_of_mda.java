import java.util.*;
class  static_way_of_mda
{
	public static void main(String[] args) 
	{
		int a[][]={ {3,4,5},
			        {7,3,2},
			        {9,1,6}};
		
		
		System.out.println("The Multi dimensional array address:");
		System.out.println(a);
		System.out.println("The Multi dimensional 1st array address:");
		System.out.println(a[0]);
		System.out.println("The Multi dimensional 2nd array address:");
		System.out.println(a[1]);
		System.out.println("The Multi dimensional 3rd array address:");
		System.out.println(a[2]);


	}
}
